<h1>The Morgana Project</h1>

<h2>What is Morgana</h2>

Morgana will be an Artifical Intelligence Simulator. Especially in the
field of so called "soft computing" - which means neural networks, fuzzy
controls and genetic algorithms. Yet Morgana targets neural networks,
fuzzy controls will follow.<p>

The concept behind that is "the connection machine" which represents all
entities as objects which can be connected to transfer single values
between them. That's the way neural networks work, but I think fuzzy
controls can be accessed the same way. 


<h2>State of the Project</h2>

In means of functionality: Morgana has much of code and even more drafts
and design (most of it must yet be written down), but only few
functionality. If you enjoy moving around blue boxes with black lines
between them you may have much fun with it, but for those who want to see
real networks: sorry it needs much time and development till Morgana can
satisfy you.

